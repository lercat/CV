<article class="clear">
  <h2>Économie Solidaire :</h2>
  <img class="right" src="assets/images/inforoots/ordis.jpg" alt="ordis en vrac" />
  <p> La politique de réductions des aides financières au secteur associatif du gouvernement
    actuel nous amène plus rapidement que prévu au commerce. Nous ne lâcherons pas
