<article>
  <h2>C'était quoi l'association Inforoots ? </h2>

  Une association participant au développement de l'internet à Marseille et ailleurs
  en offrant un support à ses adhérents, pour se connecter, trouver une aide technique,
  apprendre à créer son site, et une foule d'autres choses que l'on peut faire
  avec son ordinateur mais dont on ne se doute même pas !
</article>



<article class="clear">
  <h2>Philosophie :</h2>
  <img class="left" src="assets/images/inforoots/01-noailles.png" alt="cathy en action" />
  <p>Internet est un média existant depuis plus d'une vingtaine
    d'années, allant du particulier au particulier, en éliminant les intermédiaires,
    et à un coût d'édition minime. Ce média se doit de rester gratuit, pluraliste et
    facile d'accès. C'est aussi pour maintenir ces valeurs que l'association Inforoots
    existe. Internet doit être international. Inforoots s'engage pour des projets
    concernant tous les continents.</p>
</article>
